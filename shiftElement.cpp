#include<iostream>

using namespace std;

int main() {
    long int n, s, tmp, t;
    int i, j;
    cin >> t >> n >> s;
    long int a[n];
    do {
        for (i = 0; i < n; i++) {
            cin >> a[i];
        }
        for (j = 0; j < s; j++) {
            tmp = a[n - 1];
            for (i = 1; i < n; i++) {
                a[n - i] = a[n - 1 - i];
            }
            a[0] = tmp;
        }
        for (i = 0; i < n; i++) {
            cout << a[i] << " ";
        }
        t--;
    } while (t > 0);
    return 0;
}
